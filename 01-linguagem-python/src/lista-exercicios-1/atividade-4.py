'''
4. Faça um programa que, determine o maior valor e a soma dos valores.
Obs: pode-se utiliza a lista = [1,2,3,4,5,6,7,8, 10]
'''

lista = [1, 2, 3, 4, 5, 6, 7, 8, 10]

maior_valor = max(lista)
soma_valores = sum(lista)

print(f"O maior valor da lista é: {maior_valor}")
print(f"A soma de todos os valores é: {soma_valores}")
